    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
        request.setCharacterEncoding("utf-8");
        response.setCharacterEncoding("utf-8");
        
        UserDao userDao = new UserDao();
        User user = new User();
        HttpSession sessions = request.getSession();
        String strUsername =  new String(request.getParameter("Username").getBytes("iso-8859-1"), "UTF-8");
        String strPassword =  new String(request.getParameter("Password").getBytes("iso-8859-1"), "UTF-8");
        String strUrl = "/CRUD_Example/faces/View/Content/studentmanager.jsp";
        
        try {
           user = userDao.getUser(strUsername,userDao.encryptMD5(strPassword));
           if(user != null){
               //Create sessionlogin
                sessions.setAttribute("user", user);
                if(sessions.getAttribute("url") != null)
                    response.sendRedirect((String)sessions.getAttribute("url"));
                else
                    response.sendRedirect(strUrl);
           } else {
               response.sendRedirect("/CRUD_Example/faces/View/Content/login.jsp?error=1");
           }
        } catch(Exception e) {
            e.printStackTrace();
        }
    }

