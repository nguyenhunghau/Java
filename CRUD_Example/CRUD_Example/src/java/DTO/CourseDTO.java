public class CourseDTO {

    private int id;
    private String strYearBegin;
    private String strYearEnd;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStrYearBegin() {
        return strYearBegin;
    }

    public void setStrYearBegin(String strYearBegin) {
        this.strYearBegin = strYearBegin;
    }

    public String getStrYearEnd() {
        return strYearEnd;
    }

    public void setStrYearEnd(String strYearEnd) {
        this.strYearEnd = strYearEnd;
    }

}
